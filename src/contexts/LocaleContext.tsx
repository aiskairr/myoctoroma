interface LocaleProviderProps {
  children: ReactNode;
}

export const LocaleProvider: React.FC<LocaleProviderProps> = ({ children }) => {
  const [locale, setLocale] = useState<Locale>(() => {
    const saved = localStorage.getItem('booking-locale');
    return (saved as Locale) || 'ru';
  });

  useEffect(() => {
    localStorage.setItem('booking-locale', locale);
  }, [locale]);

  const t = (key: string, variables?: Record<string, string>): string => {
    let translation = (translations[locale] as any)[key] || key;
    
    // –ó–∞–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –≤ –ø–µ—Ä–µ–≤–æ–¥–µ
    if (variables) {
      Object.keys(variables).forEach(variable => {
        translation = translation.replace(
          new RegExp(`\\{${variable}\\}`, 'g'), 
          variables[variable]
        );
      });
    }
    
    return translation;
  };

  return (
    <LocaleContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </LocaleContext.Provider>
  );
};

export const useLocale = (): LocaleContextType => {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error('useLocale must be used within a LocaleProvider');
  }
  return context;
};
